/**
 * Incremento = ++
 * Decremento = --
 * Resto      = %
 * = - / 
 */

const num1 = 10;
const num2 = 3;
console.log(num1 % num2);

let contador = 1;
contador++;
console.log(contador);
console.log(--contador);


