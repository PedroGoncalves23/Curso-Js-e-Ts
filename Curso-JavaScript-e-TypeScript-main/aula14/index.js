// ALGUNS COMANDOS COM NUMBER

let num1 = 1.15863891;
let num2 = 2.5;

console.log(num1 + num2) // exibe a conta normalmente
console.log(num1.toString() + num2) // concatena o número
console.log(num1.toFixed(2)); // definindo as casas decimais exibição
console.log(Number.isInteger(num1)) // retorna se o número é inteiro

